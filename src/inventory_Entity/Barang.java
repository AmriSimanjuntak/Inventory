package inventory_Entity;

/**
 *
 * @author Amri Simanjuntak;
 */
public class Barang {
    
    private int id_barang;
    private String kode_barang;
    private String nama_barang;
    private String kondisi_barang;
    private String jenis_barang;
    private String lokasi_barang;
    private String jumlah_barang;
    private String tanggal_masuk;
    private String masa_berlaku;

    public int getId_barang() {
        return id_barang;
    }

    public void setId_barang(int id_barang) {
        this.id_barang = id_barang;
    }
    

    public String getKode_barang() {
        return kode_barang;
    }

    public void setKode_barang(String kode_barang) {
        this.kode_barang = kode_barang;
    }

    public String getNama_barang() {
        return nama_barang;
    }

    public void setNama_barang(String nama_barang) {
        this.nama_barang = nama_barang;
    }

    public String getKondisi_barang() {
        return kondisi_barang;
    }

    public void setKondisi_barang(String kondisi_barang) {
        this.kondisi_barang = kondisi_barang;
    }

    public String getJenis_barang() {
        return jenis_barang;
    }

    public void setJenis_barang(String jenis_barang) {
        this.jenis_barang = jenis_barang;
    }

    public String getLokasi_barang() {
        return lokasi_barang;
    }

    public void setLokasi_barang(String lokasi_barang) {
        this.lokasi_barang = lokasi_barang;
    }

    public String getJumlah_barang() {
        return jumlah_barang;
    }

    public void setJumlah_barang(String jumlah_barang) {
        this.jumlah_barang = jumlah_barang;
    }

    public String getTanggal_masuk() {
        return tanggal_masuk;
    }

    public void setTanggal_masuk(String tanggal_masuk) {
        this.tanggal_masuk = tanggal_masuk;
    }

    public String getMasa_berlaku() {
        return masa_berlaku;
    }

    public void setMasa_berlaku(String masa_berlaku) {
        this.masa_berlaku = masa_berlaku;
    }
    
    
    
}
